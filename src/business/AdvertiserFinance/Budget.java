package business.AdvertiserFinance;

/**
 *
 * @author Krush
 */
public class Budget {
    
    private int budget;
    private int availBudget;
    private int remBudget;
    private String email;

    public int getBudget() {
        return budget;
    }

    public void setBudget(int budget) {
        this.budget = budget;
    }

    public int getAvailBudget() {
        return availBudget;
    }

    public void setAvailBudget(int availBudget) {
        this.availBudget = availBudget;
    }

    public int getRemBudget() {
        return remBudget;
    }

    public void setRemBudget(int remBudget) {
        this.remBudget = remBudget;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
    
    
    
    
}
