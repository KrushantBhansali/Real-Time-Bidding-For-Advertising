    public void populateEnterpriseAdminTable() {

        DefaultTableModel dtm = (DefaultTableModel) enterpriseAdminjTable1.getModel();
        dtm.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount ua : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object row[] = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = network.getName();
                    row[2] = ua.getUserName();
                    dtm.addRow(row);
                }
            }
        }

    }

    public void populateNetworkCombobox() {
        networkjComboBox1.removeAllItems();
        enterprisejComboBox2.removeAllItems();

        for (Network network : system.getNetworkList()) {
            networkjComboBox1.removeAllItems();
            networkjComboBox1.addItem(network);
        }

    }

    public void populateEnterpriseCombobox(Network network) {
        enterprisejComboBox2.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            enterprisejComboBox2.addItem(enterprise);
        }
    }

