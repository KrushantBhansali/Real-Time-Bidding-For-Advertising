    private void submitjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitjButton1ActionPerformed
        // TODO add your handling code here:
       
        
        Network network = (Network) networkjComboBox1.getSelectedItem();
        Enterprise enterprise = (Enterprise) enterprisejComboBox2.getSelectedItem();
        String username = usernamejTextField3.getText();
        String password = passwordjTextField4.getText();
        String name = namejTextField5.getText();

       
         if (enterprise == null && network == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input");
            return;
        }

        if (usernamejTextField3.getText().equals("") || passwordjTextField4.getText().equals("") || namejTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill all the values");
            return;
        }

        for (Network n : system.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount ua : e.getUserAccountDirectory().getUserAccountList()) {
                    if (usernamejTextField3.getText().equals(ua.getUserName())) {
                        JOptionPane.showMessageDialog(null, "User Name already exists");
                        return;
                    }
                }
            }

        }

        Person employee = enterprise.getPersonDir().createNewEmployee(name);
        UserAccount ua = enterprise.getUserAccountDirectory().creatNewUserAccount(username, password, employee, new AdminRole());

        populateEnterpriseAdminTable();
    }//GEN-LAST:event_submitjButton1ActionPerformed

