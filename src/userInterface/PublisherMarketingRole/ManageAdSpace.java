    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) adSpacejTable1.getModel();
        dtm.setRowCount(0);
        System.out.println(enterprise.getSsp());

        for (AdSpace adSpace : enterprise.getSsp().getAdSpaceList()) {
            Object row[] = new Object[3];
            row[0] = adSpace;
            row[1] = adSpace.getPrice();
            row[2] = adSpace.getAvailStatus();
            dtm.addRow(row);
        }

    }

    public void populateCombo() {
        adSpacejComboBox1.removeAllItems();
        statusjComboBox1.removeAllItems();
        //  for(Organization org: enterprise.getOrganizationDir().getOrganizationList()){
        //    if(org instanceof SupplySidePlatform ){

        String status[] = {"Available", "Not Available"};
        for (int i = 0; i < status.length; i++) {
            statusjComboBox1.addItem(status[i]);
        }

        if (enterprise.getName().equals("yahoo")) {
            for (AdSpace.AdSpaceType type : AdSpace.AdSpaceType.values()) {

                if (!type.getValue().equals(AdSpace.AdSpaceType.TopRight.getValue()) && !type.getValue().equals(AdSpace.AdSpaceType.RightBottom.getValue())) {

                    adSpacejComboBox1.addItem(type);
                }
            }
        }

        if (enterprise.getName().equals("facebook")) {
            for (AdSpace.AdSpaceType type : AdSpace.AdSpaceType.values()) {
                if (!type.getValue().equals(AdSpace.AdSpaceType.Center.getValue()) && !type.getValue().equals(AdSpace.AdSpaceType.LeftBottom.getValue())) {
                    adSpacejComboBox1.addItem(type);
                }

            }

        }

    }

