    private void savejButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savejButton1ActionPerformed
        // TODO add your handling code here:

        int selectedRow = adSpacejTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a Row");
            return;
        }

        AdSpace adSpace = (AdSpace) adSpacejTable1.getValueAt(selectedRow, 0);

        if(pricejTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill all the values");
            return;
        }
        
        if (Validation.checkInteger(pricejTextField1.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Enter only Integer values");
            return;
        }

        int price = Integer.parseInt(pricejTextField1.getText());
        AdSpace.AdSpaceType type = (AdSpace.AdSpaceType) adSpacejComboBox1.getSelectedItem();
        String status = (String) statusjComboBox1.getSelectedItem();

        if (price <= 0) {
            JOptionPane.showMessageDialog(null, "Enter only valid values");
            return;
        }
        
        
       for(AdSpace ads : enterprise.getSsp().getAdSpaceList()){
            if(!ads.getPosition().equals(type.getValue())){
                JOptionPane.showMessageDialog(null,"Cannot modify this adspace");
                return;
            }
        }
        

        adSpace.setPrice(price);
        adSpace.setPosition(type.getValue());
        adSpace.setAvailStatus(status);

        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {

                if (enterprise instanceof AdExchange) {
                    for (WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()) {
                        if (request instanceof AdSpaceAvailable) {
                            AdSpaceAvailable adreq = (AdSpaceAvailable) request;
                            adreq.setStatus(status);
                        }
                    }
                    // enterprise.getWorkQueue().getWorkRequestList().add(request);
                    // userAccount.getWorkQueue().getWorkRequestList().add(request);
                }

            }

        }

        populateTable();
    }//GEN-LAST:event_savejButton1ActionPerformed

