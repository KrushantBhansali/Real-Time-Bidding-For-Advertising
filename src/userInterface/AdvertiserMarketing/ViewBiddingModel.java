    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = bidjTable1.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please Select a Row");
        }
        
        Bid bid = (Bid) bidjTable1.getValueAt(selectedRow,0);
        
        
        UpdateBiddingRule ubr = new UpdateBiddingRule(userProcessContainer,bid,organization, advertiser,userAccount, system);
        userProcessContainer.add("UpdateBiddingRule", ubr);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        
                
    }//GEN-LAST:event_jButton2ActionPerformed

