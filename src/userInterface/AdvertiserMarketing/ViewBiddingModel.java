    public void populateBidTable(){
        DefaultTableModel dtm = (DefaultTableModel) bidjTable1.getModel();
        dtm.setRowCount(0);
        
        for(Bid b : advertiser.getDsp().getBidList()){
            Object row[]= new Object[10];
            row[0]= b;
            row[1]= b.getPosition();
            row[2]= b.getMinAge();
            row[3]= b.getMaxAge();
            row[4]= b.getHobbies();
            row[5]= b.getCountry();
            row[6]= b.getLastSearched();
            row[7]= b.getMinScore();
            row[8]= b.getMaxScore();
            
            row[9]= b.getBidAmount();
            dtm.addRow(row);
        }
        
    }
